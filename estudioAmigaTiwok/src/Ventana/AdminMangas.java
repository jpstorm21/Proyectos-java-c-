    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int column = jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / jTable1.getRowHeight();
        if (row < jTable1.getRowCount() && row >= 0 && column < jTable1.getColumnCount() && column >= 0) {
            Object value = jTable1.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;
                if (boton.getText().equals("Modificar")) {
                    int cod = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    String sql;
                    Statement st;
                    ResultSet rs;
                    sql = "SELECT m.nombre,m.mangaka,m.genero,m.estado,m.idioma,m.tomo FROM mangas m where m.codigo=" + "\"" + cod + "\"";
                    RegistrarManga rm = new RegistrarManga(this.conexion,cod,this);
                    try {
                        st = conexion.getConnection().createStatement();
                        rs = st.executeQuery(sql);
                        while (rs.next()) {
                            rm.getjTextField1().setText(rs.getString(1));
                            rm.getjTextField4().setText(rs.getString(2));
                            rm.getjTextField2().setText(rs.getString(3));
                            rm.getjTextField3().setText(rs.getString(6));
                            rm.getjTextField7().setText(rs.getString(4));
                            rm.getjTextField5().setText(rs.getString(5));
                            rm.getjLabel1().setText("Modificaci√≥n de manga");
                            rm.getjButton1().setText("Actualizar");
                        }
                        rm.setVisible(true);
                    } catch (SQLException ex) {
                        Logger.getLogger(AdminMangas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else if(boton.getText().equals("Eliminar")){
                    int cod = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    String sql;
                    Statement st;
                    sql = "DELETE FROM mangas where codigo=" + cod;
                    try {
                        st = conexion.getConnection().prepareStatement(sql);
                        st.execute(sql);
                        JOptionPane.showMessageDialog(null, "Manga Eliminado");
                        refrescarTablaMangas();
                    } catch (SQLException ex) {
                        Logger.getLogger(AdminMangas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

