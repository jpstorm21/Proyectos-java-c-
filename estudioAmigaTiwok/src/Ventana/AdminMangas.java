    private static void Clear_Table1(JTable tabla) {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        for (int i = 0; i < tabla.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }
    
    public void refrescarTablaMangas() {
        Clear_Table1(jTable1);
        JButton editar = new JButton("Modificar");
        JButton eliminar = new JButton("Eliminar");
        String sql;
        Statement st;
        ResultSet rs;
        sql = "SELECT m.codigo,m.nombre,m.mangaka,m.genero,m.estado,m.idioma,m.tomo FROM mangas m";
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        try {
            st = conexion.getConnection().createStatement();
            rs = st.executeQuery(sql);
            Object[] datosQuery = new Object[9];
            while (rs.next()) {
                datosQuery[0] = rs.getInt(1);
                datosQuery[1] = rs.getString(2);
                datosQuery[2] = rs.getString(3);
                datosQuery[3] = rs.getString(4);
                datosQuery[4] = rs.getString(5);
                datosQuery[5] = rs.getString(6);
                datosQuery[6] = rs.getInt(7);
                datosQuery[7] = editar;
                datosQuery[8] = eliminar;
                modelo.addRow(datosQuery);
            }
            jTable1.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(AdminMangas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

