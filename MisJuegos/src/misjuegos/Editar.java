    public void modificarTxt() throws FileNotFoundException, IOException {
        ArrayList<Juego> l = new ArrayList();
        Scanner sc = new Scanner(new File("games.txt"));
        while (sc.hasNextLine()) {
            String leelinea = sc.nextLine();
            String[] linea = leelinea.split(",");
            if (linea[0].equalsIgnoreCase(juego.getNombre())) {
                l.add(juego);
            } else {
                String n = linea[0];
                String e = linea[1];
                if (e.equalsIgnoreCase("no me gusta")) {
                    Juego j = new Juego(e, n, "densitalado", "PS4");
                    l.add(j);

                } else {
                    String i = linea[2];
                    Juego ju = new Juego(e, n, i, "PS4");
                    l.add(ju);
                }
            }
        }
        Out salida = new Out("games.txt");
        for (int i = 0; i < l.size(); i++) {
            if (l.get(i).getEstado().equalsIgnoreCase("no me gusta")) {
                salida.println(l.get(i).getNombre() + "," + l.get(i).getEstado() + "," + l.get(i).getInstalado());
            } else {
                salida.println(l.get(i).getNombre() + "," + l.get(i).getEstado() + "," + l.get(i).getInstalado());
            }
        }

    }
