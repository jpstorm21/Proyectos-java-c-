    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String estado = jTextField2.getText();
        String instalado = jTextField3.getText();
        String plataforma = jTextField4.getText();
        if (jTextField1.getText().equalsIgnoreCase("") || jTextField2.getText().equalsIgnoreCase("") || jTextField3.getText().equalsIgnoreCase("") || jTextField4.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "tiene algun parametro vacio");

        } else {
            juego.setNombre(nombre);
            juego.setEstado(estado);
            juego.setPlataforma(plataforma);
            juego.setInstalado(instalado);
            try {
                modificarTxt();
            } catch (IOException ex) {
                Logger.getLogger(registrar.class.getName()).log(Level.SEVERE, null, ex);
            }

            this.setVisible(false);
            lista l = null;
            try {
                l = new lista();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(registrar.class.getName()).log(Level.SEVERE, null, ex);
            }

            l.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

